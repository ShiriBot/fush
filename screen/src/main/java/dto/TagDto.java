package dto;

public class TagDto {
	private String seqno;
	private String name;
	private String top;
	private String mid;
	private String top_seq;
	private String mid_seq;
	
	public String getSeqno() {
		return seqno;
	}
	public void setSeqno(String seqno) {
		this.seqno = seqno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTop() {
		return top;
	}
	public void setTop(String top) {
		this.top = top;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getTop_seq() {
		return top_seq;
	}
	public void setTop_seq(String top_seq) {
		this.top_seq = top_seq;
	}
	public String getMid_seq() {
		return mid_seq;
	}
	public void setMid_seq(String mid_seq) {
		this.mid_seq = mid_seq;
	}
}
